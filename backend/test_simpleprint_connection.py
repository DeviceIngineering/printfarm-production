                print(f"  –ö–ª—é—á–∏ –≤–µ—Ä—Ö–Ω–µ–≥–æ —É—Ä–æ–≤–Ω—è: {list(data.keys())}")

                # –ü–æ–¥—Ä–æ–±–Ω—ã–π –∞–Ω–∞–ª–∏–∑ –∫–∞–∂–¥–æ–≥–æ –∫–ª—é—á–∞
                for key, value in data.items():
                    print(f"\n  üìÅ –ö–ª—é—á '{key}':")
                    print(f"     –¢–∏–ø: {type(value)}")
